//! Tray icon constants, menu-item IDs, and the `icon_item` builder.
//!
//! Kept separate from `tray.rs` so the builder logic there can focus on
//! menu composition rather than asset wiring. The PNG bytes are baked in
//! at compile time from `src-tauri/icons/menu/` — changes to the source
//! assets require a rebuild.

use std::sync::atomic::{AtomicBool, Ordering};
use std::sync::OnceLock;
use tauri::image::Image;
use tauri::menu::{IconMenuItem, IconMenuItemBuilder};
use tauri::{AppHandle, Wry};

/// True when dropdown menus render on a dark material (system
/// appearance = Dark). Written by [`install_menu_appearance_watcher`]
/// (setup + every `ThemeChanged`); read by [`MenuGlyph::image`] at
/// menu-build time, which may run on a tokio worker — hence an atomic
/// instead of an AppKit query (NSApplication is main-thread-only).
static MENU_APPEARANCE_DARK: AtomicBool = AtomicBool::new(false);

/// A tray-menu glyph with one PNG per menu appearance.
///
/// muda 0.19 never calls `setTemplate:YES` on custom menu-item
/// bitmaps (verified against the registry source — `to_nsimage` /
/// `menuitem_set_icon` set the image verbatim), so AppKit can't
/// auto-tint a single bitmap between Light and Dark dropdowns. A
/// dark-gray stroke disappears on a Dark menu and vice versa; the
/// fix is a paired asset set selected per system appearance.
///
/// Decoding is cached per variant in a `OnceLock` — the bytes are
/// compile-time constants, so the decoded RGBA is identical for the
/// process lifetime, and tray rebuilds (live-session churn, account
/// changes, cc_doctor ticks) stop re-parsing ~20 PNGs each time.
pub struct MenuGlyph {
    /// Dark stroke (`#3a3a3a`) — rendered on LIGHT dropdowns.
    dark_stroke: &'static [u8],
    /// Light stroke (`#d0d0d0`) — rendered on DARK dropdowns.
    light_stroke: &'static [u8],
    dark_cache: OnceLock<Image<'static>>,
    light_cache: OnceLock<Image<'static>>,
}

impl MenuGlyph {
    const fn new(dark_stroke: &'static [u8], light_stroke: &'static [u8]) -> Self {
        Self {
            dark_stroke,
            light_stroke,
            dark_cache: OnceLock::new(),
            light_cache: OnceLock::new(),
        }
    }

    /// Decode (or fetch from cache) the variant matching the current
    /// menu appearance. `Image` is a cheap clone — the RGBA buffer is
    /// `Cow`-owned and clone shares no NSImage state.
    pub fn image(&self) -> Result<Image<'static>, String> {
        let (bytes, cache) = if MENU_APPEARANCE_DARK.load(Ordering::Relaxed) {
            (self.light_stroke, &self.light_cache)
        } else {
            (self.dark_stroke, &self.dark_cache)
        };
        if let Some(img) = cache.get() {
            return Ok(img.clone());
        }
        let img = Image::from_bytes(bytes).map_err(|e| format!("menu glyph decode: {e}"))?;
        // A racing decode of the same constant bytes is identical;
        // losing the `set` race just means we hand back our copy.
        let _ = cache.set(img.clone());
        Ok(img)
    }
}

/// Seed the appearance flag and keep it current. Call once during
/// setup (main thread), after the main window exists.
///
/// The window's theme tracks the system appearance (nothing in this
/// app overrides the native window theme — the in-app `data-theme`
/// flip is CSS-only), and tray dropdown menus follow the system
/// appearance too, so `WindowEvent::ThemeChanged` is the right
/// cross-platform change signal. On a flip the tray is rebuilt so
/// every glyph swaps to the readable stroke variant.
pub fn install_menu_appearance_watcher(app: &AppHandle) {
    use tauri::Manager;
    let Some(window) = app.get_webview_window("main") else {
        // Test harness / headless: leave the default (light) in place.
        return;
    };
    let dark = matches!(window.theme(), Ok(tauri::Theme::Dark));
    MENU_APPEARANCE_DARK.store(dark, Ordering::Relaxed);

    let app = app.clone();
    window.on_window_event(move |event| {
        if let tauri::WindowEvent::ThemeChanged(theme) = event {
            let dark = matches!(theme, tauri::Theme::Dark);
            let prev = MENU_APPEARANCE_DARK.swap(dark, Ordering::Relaxed);
            if prev != dark {
                let handle = app.clone();
                tauri::async_runtime::spawn(async move {
                    if let Err(e) = crate::tray::rebuild(&handle).await {
                        tracing::warn!("tray rebuild after theme change failed: {e}");
                    }
                });
            }
        }
    });
}

// Lucide-sourced glyph icons. Generated by
// `scripts/generate-menu-icons.sh` from `node_modules/lucide-static/`
// at 96px content centered in a 144px transparent canvas, stroke 1.5.
// Each glyph ships two stroke variants: `{name}.png` (#3a3a3a, ~22%
// luminance — targets Light dropdowns; macOS NSMenu Vibrant Light
// material is a mid-gray ≈ rgb(160,160,160), so the prior `#888`
// stroke read as invisible) and `{name}-light.png` (#d0d0d0 — targets
// Dark dropdowns). `MenuGlyph::image` picks per system appearance.
// Re-run the script after upgrading `lucide-static` or tweaking the
// stroke / padding knobs.
macro_rules! menu_glyph {
    ($name:literal) => {
        MenuGlyph::new(
            include_bytes!(concat!("../icons/menu/", $name, ".png")),
            include_bytes!(concat!("../icons/menu/", $name, "-light.png")),
        )
    };
}

