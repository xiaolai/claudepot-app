/// Build an icon menu item from a pre-rendered glyph pair.
pub fn icon_item(
    app: &AppHandle,
    id: &str,
    label: &str,
    glyph: &'static MenuGlyph,
) -> Result<IconMenuItem<Wry>, String> {
    let img = glyph.image().map_err(|e| format!("icon {id}: {e}"))?;
    IconMenuItemBuilder::with_id(id, label)
        .icon(img)
        .build(app)
        .map_err(|e| format!("icon item {id}: {e}"))
}

#[cfg(test)]
mod tests {
    use super::*;

    /// Both stroke variants of every glyph must decode — a broken or
    /// missing asset should fail in CI, not at menu-build time on a
    /// user's machine (where `icon_item` errors abort the rebuild).
    #[test]
    fn test_menu_glyph_both_variants_decode() {
        let glyphs: [(&str, &MenuGlyph); 17] = [
            ("terminal", &ICON_TERMINAL),
            ("desktop", &ICON_DESKTOP),
            ("user-plus", &ICON_USER_PLUS),
            ("refresh", &ICON_REFRESH),
            ("home", &ICON_HOME),
            ("sliders", &ICON_SLIDERS),
            ("check", &ICON_CHECK),
            ("power", &ICON_POWER),
            ("bar-chart", &ICON_BAR_CHART),
            ("bolt", &ICON_BOLT),
            ("badge-check", &ICON_BADGE_CHECK),
            ("layers", &ICON_LAYERS),
            ("shield", &ICON_SHIELD),
            ("circle-user", &ICON_CIRCLE_USER),
            ("circle-play", &ICON_CIRCLE_PLAY),
            ("circle-pause", &ICON_CIRCLE_PAUSE),
            ("circle-dot", &ICON_CIRCLE_DOT),
        ];
        for (name, glyph) in glyphs {
            assert!(
                Image::from_bytes(glyph.dark_stroke).is_ok(),
                "{name}: dark-stroke PNG failed to decode"
            );
            assert!(
                Image::from_bytes(glyph.light_stroke).is_ok(),
                "{name}: light-stroke PNG failed to decode"
            );
        }
    }

    /// The decode cache must return the same pixel dimensions as a
    /// fresh decode, and repeated calls must keep working (the cache
    /// is shared process state; this also exercises the clone path).
    #[test]
    fn test_menu_glyph_image_caches_and_clones() {
        let first = ICON_CHECK.image().expect("first decode");
        let second = ICON_CHECK.image().expect("cached decode");
        assert_eq!(first.width(), second.width());
        assert_eq!(first.height(), second.height());
        assert_eq!(first.width(), 144, "menu glyphs are 144px canvases");
    }
}
